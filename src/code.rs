#[repr(C)]
#[derive(Debug, Clone, Copy, PartialEq)]
pub enum MyHotKeyState {
    Pressed = 0,
    Released = 1,
}
impl From<MyHotKeyState> for c_uchar {
    fn from(value: MyHotKeyState) -> Self {
        value as Self
    }
}
impl TryFrom<c_uchar> for MyHotKeyState {
    type Error = Status;

    fn try_from(value: c_uchar) -> Result<Self, Self::Error> {
        match value {
            0 => Ok(MyHotKeyState::Pressed),
            1 => Ok(MyHotKeyState::Released),
            _ => Err(Status::Fail),
        }
    }
}

