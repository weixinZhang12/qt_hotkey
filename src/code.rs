/// 将 u8 数字安全地转换为 Modifiers 枚举
pub fn digit_to_modifier(value: c_uchar) -> Option<Modifiers> {
    Some(match value {
        0 => Modifiers::ALT,
        1 => Modifiers::ALT_GRAPH,
        2 => Modifiers::CAPS_LOCK,
        3 => Modifiers::CONTROL,
        4 => Modifiers::FN,
        5 => Modifiers::FN_LOCK,
        6 => Modifiers::META,
        7 => Modifiers::NUM_LOCK,
        8 => Modifiers::SCROLL_LOCK,
        9 => Modifiers::SHIFT,
        10 => Modifiers::SYMBOL,
        11 => Modifiers::SYMBOL_LOCK,
        12 => Modifiers::HYPER,
        13 => Modifiers::SUPER,
        _ => return None,
    })
}

